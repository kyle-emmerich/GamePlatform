static const size_t KeyCodeMapSize = 0xDF;

inline KeyCode Win32VirtualKeyToKeyCode(uint32_t vkCode) {
    if (vkCode >= (uint32_t)KeyCodeMapSize) {
        return KeyCode::None;
    }
    return virtualKeyCodeToKeyCodeMap[vkCode];
}